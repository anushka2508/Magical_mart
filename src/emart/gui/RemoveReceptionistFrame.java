    private void loadReceptionistId() {
        try{
            List<String> receptionistListId=ReceptionistDAO.getAllReceptionistUserId();
            if(receptionistListId.isEmpty())
            {
                JOptionPane.showMessageDialog(null,"No record present");
            }
            btnRemove.setEnabled(true);
            for(String userid: receptionistListId)
            {
                jcRecep.addItem(userid);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Db Error!");
            ex.printStackTrace();
        }
    }
