    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
      String userid=jcRecep.getSelectedItem().toString();
        try{
           boolean result=ReceptionistDAO.deleteReceptionist(userid);
           if(result)
           {  
            JOptionPane.showMessageDialog(null,"Receptionist deleted");
             jcRecep.removeItemAt(jcRecep.getSelectedIndex());
             if(jcRecep.getItemCount()==0)
                 btnRemove.setEnabled(false);
             return;
           }
           JOptionPane.showMessageDialog(null,"Receptionist not deleted");       
      }
          catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Please Try Again Later","Error in DB",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

