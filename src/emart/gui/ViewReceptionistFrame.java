    private void loadReceptionistDetails() {
               try{
           List<ReceptionistPojo> emplist=ReceptionistDAO.getAllReceptionistDetails(); 
           DefaultTableModel tm=(DefaultTableModel)jtAllReceptionist.getModel();
           Object [] rows=new Object[5];
           for(ReceptionistPojo emp:emplist)
           {
               rows[0]=emp.getEmpid();
               rows[1]=emp.getEmpname();
               rows[2]=emp.getUserid();
               rows[3]=emp.getJob();
               rows[4]=emp.getSalary();
               tm.addRow(rows);
           }
       }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Please Try Again Later","Error in DB",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
