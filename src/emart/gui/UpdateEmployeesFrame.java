    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(!validateInputs())
        {
            JOptionPane.showMessageDialog(null,"Please Input all the feilds","ERROR",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
//           String id=txtCombo1.getSelectedItem().toString();
//           String name=txtEmpName.getText();
//           String job=txtCombo.getSelectedItem().toString();
//           Double sal=Double.parseDouble(txtSalary.getText());
           EmployeePojo e=new EmployeePojo();
//            System.out.println("My id:"+id);
            e.setEmpid(txtCombo1.getSelectedItem().toString());
//            System.out.println("My id:"+e.getEmpid());
            e.setEmpname(txtEmpName.getText());
            e.setEmpjob(txtCombo.getSelectedItem().toString());
            e.setSalary(Double.parseDouble(txtSalary.getText()));            
            boolean result=EmployeeDAO.updateEmployee(e);
            if(!result)
            {
                JOptionPane.showMessageDialog(null,"Record not inserted","ERROR",JOptionPane.ERROR_MESSAGE);
                return;
            }
            else{
            JOptionPane.showMessageDialog(null,"Record  inserted","Success",JOptionPane.INFORMATION_MESSAGE);
            clearText();
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Please Try Again Later","Error in DB",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();

        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null,"Please input Numeric value for salary","Error in DB",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();

        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void txtCombo1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCombo1ActionPerformed

       try{
            EmployeePojo e=new EmployeePojo();
            String id=(String)txtCombo1.getSelectedItem();
            if(txtCombo1.getSelectedIndex()!=0)
                e=EmployeeDAO.getAllEmp(id);
           txtEmpName.setText(e.getEmpname());
            txtCombo.setSelectedItem((Object)e.getEmpjob());
            txtSalary.setText(String.valueOf(e.getSalary()));
           
        }
         catch(SQLException ex){
          JOptionPane.showMessageDialog(null,"DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace(); 
         }
    }//GEN-LAST:event_txtCombo1ActionPerformed

