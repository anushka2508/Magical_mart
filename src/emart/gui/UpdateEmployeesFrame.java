    private void loadId() {    
        try
                {
                    txtCombo1.removeAllItems();
                     ArrayList<String>list=new ArrayList<>();
                    list=EmployeeDAO.getAllId();
                    txtCombo1.addItem("Choose ID");
                    for(String id:list)
                    {
                        txtCombo1.addItem(id);
                    }
                }
        catch(SQLException ex){
          JOptionPane.showMessageDialog(null,"DB Error!","Error!",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace(); 
         }
    }
